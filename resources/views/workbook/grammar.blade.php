                            <p>名詞的用法（～すること）</p>
                            <p>He likes <span class="underline">to play</span> tennis.（彼はテニスを<span class="underline">すること</span>が好きだ。）</p>
                        </li>
                        <li>
                            <p>副詞的用法（～するために）</p>
                            <p>She got up early <span class="underline">to run</span>.（彼女は<span class="underline">走るために</span>早起きした。）</p>
                        </li>
                        <li>
                            <p>形容詞的用法（～するための）</p>
                            <p>I want something <span class="underline">to write</span>.（私は何か<span class="underline">書くための</span>ものが欲しい。）</p>
                        </li>
                    </ul>
                </div>

