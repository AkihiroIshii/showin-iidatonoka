                <!-- 代名詞 -->
                <div class="content-block-1">
                    <h4>代名詞（中１）</h4>
                    <ul>
                       <li>
                            <p>主格</p>
                            <p><span class="underline">I</span> have a pen.（<span class="underline">私は</span>ペンを持っている。）</p>
                            <p><span class="underline">He</span> likes tennis.（<span class="underline">彼は</span>テニスが好きです。）</p>
                        </li>
                        <li>
                            <p>所有格</p>
                            <p>This is <span class="underline">my</span> pen.（これは<span class="underline">私の</span>ペンです。）</p>
                            <p>That is <span class="underline">his</span> racket.（あれは<span class="underline">彼の</span>ラケットです。）</p>
                        </li>
                        <li>
                            <p>目的格</p>
                            <p>She gave <span class="underline">me</span> this pen.（彼女は<span class="underline">私に</span>このペンをくれた。）</p>
                            <p>The tennis player knows <span class="underline">him</span>.（そのテニス選手は<span class="underline">彼を</span>知っている。）</p>
                        </li>
                    </ul>
                </div>

                <!-- 進行形 -->
                <div class="content-block-1">
                    <h4>進行形（中１）</h4>
                    <ul>
                    <li>
                            <p>現在進行形</p>
                            <p>He <span class="underline">is running</span> now.（彼は今<span class="underline">走っている</span>。）</p>
                            <p>He <span class="underline">isn't running</span> now.（彼は今<span class="underline">走っていない</span>。）</p>
                            <p><span class="underline">Is</span> he <span class="underline">running</span> now?（彼は今<span class="underline">走っていますか</span>。）</p>
                        </li>
                        <li>
                            <p>過去進行形</p>
                            <p>He <span class="underline">was running</span> then.（彼はその時<span class="underline">走っていた</span>。）</p>
                            <p>He <span class="underline">wasn't running</span> then.（彼はその時<span class="underline">走っていなかった</span>。）</p>
                            <p><span class="underline">Was</span> he <span class="underline">running</span> then?（彼はその時<span class="underline">走っていましたか</span>。）</p>
                        </li>
                    </ul>
                </div>

                <!-- 接続詞 -->
                <div class="content-block-1">
                    <h4>接続詞（中２）</h4>
                    <ul>
                    <li>
                            <p>He lived in Osaka. + He was ten years old <span class="underline">then</span>.</p>
                            <p>⇒ He lived in Osaka <span class="underline">when</span> he was ten years old.</p>
                            <p>（彼は10才<span class="underline">のとき、</span>大阪に住んでいた。）</p>
                        </li>
                        <li>
                            <p>This book is difficult. + I think <span class="underline">so</span>.</p>
                            <p>⇒ I think <span class="underline">that</span> this book is difficult.</p>
                            <p>（私は、この本は難しい<span class="underline">と</span>思う。）</p>
                        </li>
                    </ul>
                </div>

